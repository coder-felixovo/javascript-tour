// 静态方法 `Math.floor()` 返回小于或等于给定数字的最大整数

console.log(Math.floor(6.01)) // 6

console.log(Math.floor(6.99)) // 6

console.log(Math.floor(6)) // 6

console.log(Math.floor(-6.01)) // -7

console.log(Math.floor(-6.99)) // -7

console.log(Math.floor(-0)) // -0

console.log(Math.floor(0)) // 0

console.log(Math.floor(-Infinity)) // -Infinity

console.log(Math.floor(Infinity)) // Infinity

console.log(Math.floor(-NaN)) // NaN

console.log(Math.floor(NaN)) // NaN